def fill(template: str, index: int) -> str:
    text = template
    for key, values in SLOTS.items():
        token = "{" + key + "}"
        if token in text:
            text = text.replace(token, values[(index + SLOT_OFFSETS.get(key, 0)) % len(values)])
    return text


def norm(text: str) -> str:
    return (
        text.replace("\n", " ")
        .replace("\r", " ")
        .replace("、、", "、")
        .replace("？？", "？")
        .replace("。。", "。")
        .replace("、？", "？")
        .replace("今からに", "今から")
        .replace("このあとに", "このあと")
        .replace("今日に", "今日")
        .replace("今晩に", "今晩")
        .replace("夜に紹介", "夜、紹介")
        .replace("後後", "後")
        .strip()
    )


def with_suffix(base: str, suffix: str) -> str:
    if not suffix:
        return base
    if base.endswith("？"):
        return base[:-1] + "、" + suffix
    return base + "、" + suffix


def add(target: list[str], seen: set[tuple[str, str]], intent: str, text: str) -> bool:
    value = norm(text)
    if not value or value.lower().startswith(("=", "+", "-", "@")):
        return False
    if len(value) > 96:
        value = value[:95] + "…"
    key = (intent, value)
    if key in seen:
        return False
    seen.add(key)
    target.append(value)
    return True


def make_examples(intent: str, cfg: dict[str, list[str]], target_count: int = 300) -> list[str]:
    examples: list[str] = []
    seen: set[tuple[str, str]] = set()
    seeds: list[str] = []

    for text in cfg["direct"] + cfg.get("styles", []):
        seeds.append(norm(text))
    for index in range(120):
        for template in cfg["templates"]:
            seeds.append(norm(fill(template, index)))

    unique_seeds: list[str] = []
    seen_seed: set[str] = set()
    for seed in seeds:
        if seed and seed not in seen_seed:
            seen_seed.add(seed)
            unique_seeds.append(seed)
    seeds = unique_seeds

    for seed in seeds:
        add(examples, seen, intent, seed)
    for seed in seeds:
        for prefix in PREFIXES:
            if prefix:
                add(examples, seen, intent, prefix + seed)
            if len(examples) >= target_count:
                random.shuffle(examples)
                return examples[:target_count]
    for seed in seeds:
        for suffix in SUFFIXES:
            if suffix:
                add(examples, seen, intent, with_suffix(seed, suffix))
            if len(examples) >= target_count:
                random.shuffle(examples)
                return examples[:target_count]

    index = 0
    while len(examples) < target_count and index < 10000:
        base = seeds[index % len(seeds)]
        phrase = with_suffix(PREFIXES[(index * 3) % len(PREFIXES)] + base, SUFFIXES[(index * 5) % len(SUFFIXES)])
        phrase = with_suffix(phrase, EXTRA_ENDINGS[(index * 7) % len(EXTRA_ENDINGS)])
        add(examples, seen, intent, phrase)
        index += 1

    if len(examples) < target_count:
        raise RuntimeError(f"{intent}: only {len(examples)} examples")
    random.shuffle(examples)
    return examples[:target_count]


def resolve_global_duplicates(rows: list[list[str]]) -> None:
    used: set[str] = set()
    for row in rows:
        intent, example = row
        candidate = example
        if candidate in used:
            for qualifier in QUALIFIERS[intent] + EXTRA_ENDINGS + SUFFIXES:
                if not qualifier:
                    continue
                fixed = with_suffix(example, qualifier)
                if fixed not in used:
                    candidate = fixed
                    break
        cursor = 0
        while candidate in used:
            qualifier = QUALIFIERS[intent][cursor % len(QUALIFIERS[intent])]
            fixed = with_suffix(candidate, qualifier)
            if fixed not in used:
                candidate = fixed
                break
            cursor += 1
        row[1] = candidate
        used.add(candidate)


